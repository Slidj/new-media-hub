export const getLanguage = (code?: string): Language => {
  if (!code) return 'en';
  if (code.startsWith('uk') || code.startsWith('ua')) return 'uk';
  if (code.startsWith('ru') || code.startsWith('be')) return 'ru';
  return 'en';
};
